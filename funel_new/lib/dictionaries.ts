const GENDER_MAP: Record<Gender, string> = {
  male: 'man',
  female: 'woman',
}

// –§—É–Ω–∫—Ü–∏—è –ø–æ–ª—É—á–µ–Ω–∏—è –≤—Å–µ—Ö –≤–æ–∑–º–æ–∂–Ω—ã—Ö –ø—É—Ç–µ–π –∫ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—é —Ü–≤–µ—Ç–æ—Ç–∏–ø–∞ (–¥–ª—è –ø–µ—Ä–µ–±–æ—Ä–∞)
export function getColorSeasonImagePaths(
  color_season: ColorSeason,
  gender?: Gender,
  age?: Age
): string[] {
  const folderInfo = SEASON_FOLDER_MAP[color_season]
  if (!folderInfo) {
    console.log('getColorSeasonImagePaths: Invalid color_season', { color_season })
    return []
  }

  const basePath = `/foto/types/${folderInfo.season}/${folderInfo.subtype}`

  // –ï—Å–ª–∏ gender –∏–ª–∏ age –Ω–µ —É–∫–∞–∑–∞–Ω—ã, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º fallback –≤–∞—Ä–∏–∞–Ω—Ç—ã
  if (!gender || !age) {
    console.log('getColorSeasonImagePaths: Missing gender or age, using fallback', { gender, age })
    const fallbackPaths: string[] = []
    
    // –î–ª—è bright_winter –¥–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥–¥–µ—Ä–∂–∫—É —Ñ–∞–π–ª–∞ "bright winter.webp"
    if (color_season === 'bright_winter') {
      fallbackPaths.push(`${basePath}/bright winter.webp`)
      fallbackPaths.push(`${basePath}/bright-winter.webp`)
    }
    
    // –î–æ–±–∞–≤–ª—è–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã –∫–∞–∫ fallback
    const fallbackFiles = ['man-young', 'man-old', 'woman-young', 'woman-old']
    const extensions = ['.webp', '.jpg']
    for (const filename of fallbackFiles) {
      for (const ext of extensions) {
        fallbackPaths.push(`${basePath}/${filename}${ext}`)
      }
    }
    
    return fallbackPaths
  }

  // –§–æ—Ä–º–∏—Ä—É–µ–º —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–µ –∏–º–µ–Ω–∞ —Ñ–∞–π–ª–æ–≤ (–±–µ–∑ –æ–ø–µ—á–∞—Ç–æ–∫, —Ç–∞–∫ –∫–∞–∫ –≤—Å–µ —Ñ–∞–π–ª—ã –ø–µ—Ä–µ–∏–º–µ–Ω–æ–≤–∞–Ω—ã)
  const filenameVariants: string[] = []

  if (gender === 'male' && age === 'young') {
    filenameVariants.push('man-young')
  } else if (gender === 'male' && age === 'old') {
    filenameVariants.push('man-old')
  } else if (gender === 'female' && age === 'young') {
    filenameVariants.push('woman-young')
  } else if (gender === 'female' && age === 'old') {
    filenameVariants.push('woman-old')
  }

  // –†–∞—Å—à–∏—Ä–µ–Ω–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ (—Å–Ω–∞—á–∞–ª–∞ –ø—Ä–æ–±—É–µ–º .webp, –ø–æ—Ç–æ–º .jpg)
  const extensions = ['.webp', '.jpg']

  // –§–æ—Ä–º–∏—Ä—É–µ–º –≤—Å–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ –ø—É—Ç–∏
  const paths: string[] = []
  for (const filename of filenameVariants) {
    for (const ext of extensions) {
      paths.push(`${basePath}/${filename}${ext}`)
    }
  }

  // –î–ª—è bright_winter –¥–æ–±–∞–≤–ª—è–µ–º –ø–æ–¥–¥–µ—Ä–∂–∫—É —Ñ–∞–π–ª–∞ "bright winter.webp" –∫–∞–∫ –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–π –≤–∞—Ä–∏–∞–Ω—Ç
  if (color_season === 'bright_winter') {
    paths.push(`${basePath}/bright winter.webp`)
    paths.push(`${basePath}/bright-winter.webp`)
  }

  console.log('getColorSeasonImagePaths result:', {
    color_season,
    gender,
    age,
    basePath,
    filenameVariants,
    paths,
  })

  return paths
}

// –§—É–Ω–∫—Ü–∏—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø–µ—Ä–≤–æ–≥–æ –≤–æ–∑–º–æ–∂–Ω–æ–≥–æ –ø—É—Ç–∏ –∫ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏—é —Ü–≤–µ—Ç–æ—Ç–∏–ø–∞
export function getColorSeasonImagePath(
  color_season: ColorSeason,
  gender?: Gender,
  age?: Age
): string | null {
  const paths = getColorSeasonImagePaths(color_season, gender, age)
  return paths.length > 0 ? paths[0] : null
}

